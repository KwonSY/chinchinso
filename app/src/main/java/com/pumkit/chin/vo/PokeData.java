public class PokeData {

    private String fr_id, fr_name, fr_pic;
    private String poke_id, poke_name, poke_pic;

    public String getFr_id() {
        return fr_id;
    }

    public void setFr_id(String fr_id) {
        this.fr_id = fr_id;
    }

    public String getFr_name() {
        return fr_name;
    }

    public void setFr_name(String fr_name) {
        this.fr_name = fr_name;
    }

    public String getFr_pic() {
        return fr_pic;
    }

    public void setFr_pic(String fr_pic) {
        this.fr_pic = fr_pic;
    }

    public String getPoke_id() {
        return poke_id;
    }

    public void setPoke_id(String poke_id) {
        this.poke_id = poke_id;
    }

    public String getPoke_name() {
        return poke_name;
    }

    public void setPoke_name(String poke_name) {
        this.poke_name = poke_name;
    }

    public String getPoke_pic() {
        return poke_pic;
    }

    public void setPoke_pic(String poke_pic) {
        this.poke_pic = poke_pic;
    }
}